package co.edu.uniquindio.figuras;

import java.util.ArrayList;
import java.util.List;

public abstract class Figura            
{
	/**
	 * Metodo abstracto que permita clacular el área
	 * @return: retorna el valor del área calculada
	 */	
	public abstract double calcularArea();
	
	
	/**
	 * Metodo abstracto que permite calcular perimetro
	 * @return retorna el valor del perimetro calculado
	 */
	public abstract double calcularPerimetro();
}
	

